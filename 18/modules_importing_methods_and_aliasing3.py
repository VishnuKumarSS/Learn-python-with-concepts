# normal importing method
# import module_name

# example:
import turtle
object_name = turtle.Turtle()

# from method
# from module_name import class_names,function_names,etc...

# example:
from turtle import Turtle
object_name = Turtle()

# * method
# from module_name import *
# * means ( all ).

#example:
from turtle import *
object_name = Turtle()

# aliasing modules
# import module_name as any_name

# example:
import turtle as T
object_name = T.Turtle()

